package DTO;

public class DatesDTO {
    String id;
    String available_date;
    String available_time;
    String date_availability;

    public String getAvailable_date() {
        return available_date;
    }

    public void setAvailable_date(String available_date) {
        this.available_date = available_date;
    }

    public String getAvailable_time() {
        return available_time;
    }

    public void setAvailable_time(String available_time) {
        this.available_time = available_time;
    }

    public String getDate_availability() {
        return date_availability;
    }

    public void setDate_availability(String date_avalibility) {
        this.date_availability = date_avalibility;
    }

    public String getId() {

        return id;
    }

    public void setId(String id) {

        this.id = id;
    }


}
